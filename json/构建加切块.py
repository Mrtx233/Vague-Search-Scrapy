import json
import os


def split_keywords_to_json(
        keywords_list,  # 关键词数组（Python列表）
        category,  # 类别名称（如"商务金融"、"机械制造"）
        lang="zh-CHT",  # 语种，默认zh-CHT
        chunk_size=200,  # 每个文件的关键词数量，默认200
        output_dir=r"output_json\new"  # 输出文件夹
):
    """
    将关键词列表按指定数量拆分，生成JSON文件
    JSON格式：[{"外文": "关键词", "中文": "关键词", "语种": lang, "类别": category}, ...]
    文件命名：类别_A.json、类别_B.json、类别_C.json...
    """
    # 创建输出文件夹（不存在则创建）
    if not os.path.exists(output_dir):
        os.makedirs(output_dir)

    # 拆分列表为多个子列表（每chunk_size个一组）
    chunks = [
        keywords_list[i:i + chunk_size]
        for i in range(0, len(keywords_list), chunk_size)
    ]

    # 生成字母后缀（A、B、C...）
    def get_letter_suffix(index):
        return chr(ord('A') + index)

    # 遍历每个子列表，生成JSON文件
    for idx, chunk in enumerate(chunks):
        # 构建JSON数据
        json_data = [
            {
                "外文": keyword,
                "中文": keyword,
                "语种": lang,
                "类别": category
            }
            for keyword in chunk
        ]

        # 定义文件名（如：商务金融_A.json）
        letter = get_letter_suffix(idx)
        file_name = f"{category}_{letter}.json"
        file_path = os.path.join(output_dir, file_name)

        # 写入JSON文件（保证中文正常显示，格式化输出）
        with open(file_path, 'w', encoding='utf-8') as f:
            json.dump(json_data, f, ensure_ascii=False, indent=4)

        print(f"✅ 已生成文件：{file_path}，包含{len(chunk)}个关键词")


